def rate(el):
    if el["imdb"] > 5.5:
        return True
    else:
        return False


def rate_list(movie_list):
    good_movies = []
    for i in movie_list:
        if rate(i):
            good_movies.append(i)
    return good_movies


def category(s:str):
    category_movies = [x for x in movies if x["category"] == s]
    return category_movies



def mean_point(movies):
    sum = 0
    for i in movies:
        sum = sum + i["imdb"]
    return sum / len(movies)


def mean_bycategory(s):
    return mean_point(category(s))

